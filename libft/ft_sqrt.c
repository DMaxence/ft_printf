#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb > 0)
	{
		if (nb % 2 == 0)
		{
			while (++i <= nb / 2)
			{
				if (i * i == nb)
					return (i);
			}
		}
		else
		{
			while (++i <= nb / 2)
			{
				if (i * i == nb)
					return (i);
			}
		}
	}
	return (0);
}
